  u_out%Position = u(1)%Position + b2 * t_out + c2 * t_out**2
  DEALLOCATE(b2)
  DEALLOCATE(c2)
 ELSE 
   ErrStat = ErrID_Fatal
   ErrMsg = ' order must be less than 3 in IfW_HHWind_Input_ExtrapInterp '
   RETURN
 ENDIF 
 END SUBROUTINE IfW_HHWind_Input_ExtrapInterp


