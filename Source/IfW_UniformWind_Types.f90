    IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%HSHR))-1 ) =  PACK(InData%HSHR ,.TRUE.)
    Re_Xferred   = Re_Xferred   + SIZE(InData%HSHR)
  ENDIF
